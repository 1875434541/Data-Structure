#include "stdio.h"

//************************************************************
//				函数声明
//************************************************************
void	fastsort(int *,int,int);										//快速排序;
int		findpos(int *,int,int);											//查找第一个元素插入的位置

//************************************************************
//				程序入口
//************************************************************
void main()
{
	int a[6]={2,1,0,5,4,3};
	fastsort(a,0,5);
	for (int i=0;i<6;i++)
	{
		printf("%d ",a[i]);
	}
	return;
}

