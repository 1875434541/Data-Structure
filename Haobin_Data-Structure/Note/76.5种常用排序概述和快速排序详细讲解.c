	//---------快速排序----------
void	fastsort(int *arr,int low,int high)
{
	if (low < high )
	{
		int pos=findpos(arr,low,high);
		fastsort(arr,low,pos-1);
		fastsort(arr,pos+1,high);
	}
	
	return;
}

	//--------查找位置-----------
int		findpos(int *a,int l,int h)
{
	int tmp=a[l];

	while (l < h)
	{
		while (l < h && a[h] >= tmp)
		{
			h--;
		}
		a[l]=a[h];
		while(l < h && a[l] <= tmp)
		{
			l++;
		}
		a[h]=a[l];
	}
	a[l]=tmp;
	return l;
}
